#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	negativity;

	i = 0;
	negativity = 1;
	result = 0;
	if (str == NULL)
		return (0);
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			negativity *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (str[i] - '0') + result * 10;
		i++;
	}
	return (negativity * result);
}
